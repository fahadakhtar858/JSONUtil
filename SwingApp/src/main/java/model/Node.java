package model;

/**
 *
 * @author Fahad.Akhter
 */
public class Node {
      private String key;
    private Object value;

    public Node() {
        this.key = "";
        this.value = "";
    }


    public Node(String key, Object value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }
    
}
